#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;

	if (!s || !f)
		return ((char *)0);
	i = 0;
	while (s[i])
		i++;
	if (!(str = (char *)ft_strnew(sizeof(char) * i)))
		return ((char *)0);
	i = 0;
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	return (str);
}
